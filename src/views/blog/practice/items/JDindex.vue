<script>
export default {
  mounted() {
    // ;(function() {
    //   function setBannerHeight() {
    //     document.querySelector('.jd-banner').style.height =
    //       document.querySelector('.jd-banner ul img').offsetHeight + 'px'
    //   }
    //   setBannerHeight()
    //   window.onresize = function() {
    //     setBannerHeight() //设置高度
    //   }
    // })()
    ;(function() {
      // 滑动屏幕，头部变色
      setHeader()
      // 秒杀倒计时
      downTime()
      //新闻滚动
      news()
      //自动轮播图
      banner()

      // 秒杀倒计时
      function downTime() {
        var t = 5 * 60 * 60

        var timeid = setInterval(function() {
          var h = Math.floor(t / 3600)
          var m = Math.floor((t % 3600) / 60)
          var s = t % 60
          t--

          h = h < 10 ? '0' + h : h
          m = m < 10 ? '0' + m : m
          s = s < 10 ? '0' + s : s

          var spans = document.querySelectorAll(
            '.jd-seckill .title .time span:nth-child(2n+1)'
          )
          if (!spans[0]) {
            clearInterval(timeid)
            return
          }

          spans[0].innerHTML = h
          spans[1].innerHTML = m
          spans[2].innerHTML = s
        }, 1000)
      }

      // 滑动屏幕，头部变色
      function setHeader() {
        window.addEventListener('scroll', function() {
          if (document.querySelector('.jd-banner ul img') === null) {
            return
          }
          var scrollTop = document.documentElement.scrollTop
          var bannerHeight = document.querySelector('.jd-banner ul img')
            .offsetHeight

          var value = scrollTop / bannerHeight

          if (value > 1) {
            value = 1
          }

          document.querySelector('.jd-header').style.backgroundColor =
            'rgba(240,43,43, ' + value + ')'
        })
      }

      //新闻滚动
      function news() {
        var ul = document.querySelector('.jd-new .info')
        var newsList = ul.querySelectorAll('li')

        var index = 0

        setInterval(function() {
          index++
          ul.style.transition = 'transform 1s'
          ul.style.transform = 'translateY(' + -index * 30 + 'px)'
        }, 1500)

        ul.addEventListener('transitionend', function() {
          if (index >= newsList.length - 1) {
            index = 0
            ul.style.transition = 'none'
            ul.style.transform = 'translateY(0)'
          }
        })
      }

      //自动轮播图
      function banner() {
        var banner = document.querySelector('.jd-banner')
        var ul = document.querySelector('.jd-banner ul')
        var bannersList = ul.querySelectorAll('li')
        var width = banner.offsetWidth
        var cirlist = banner.querySelectorAll('ol li')

        var index = 1

        var timer = setInterval(turn, 2000)

        ul.addEventListener('transitionend', function() {
          if (index >= bannersList.length - 1) {
            index = 1
            removeTransition()
            setTranslateX(-index * width)
          }
          if (index <= 0) {
            index = 8
            removeTransition()
            setTranslateX(-index * width)
          }
          circle(index - 1)
        })

        //小圆点
        function circle(index) {
          cirlist.forEach(function(v, i) {
            v.classList.remove('current')
          })
          cirlist[index].classList.add('current')
        }

        var startX = 0
        var moveX = 0
        var distanceX = 0

        ul.addEventListener('touchstart', function(e) {
          clearInterval(timer)
          startX = e.targetTouches[0].clientX
        })
        ul.addEventListener('touchmove', function(e) {
          moveX = e.targetTouches[0].clientX
          distanceX = moveX - startX
          removeTransition()
          setTranslateX(-index * width + distanceX)
        })
        ul.addEventListener('touchend', function() {
          if (Math.abs(distanceX) > width / 3) {
            if (distanceX > 0) {
              index--
            }
            if (distanceX < 0) {
              index++
            }
          }
          addTransition()
          setTranslateX(-index * width)

          startX = 0
          moveX = 0
          distanceX = 0

          timer = setInterval(turn, 2000)
        })
        function removeTransition() {
          ul.style.transition = 'none'
          ul.style.webkitTransition = 'none'
        }

        function addTransition() {
          ul.style.transition = 'transform .5s'
          ul.style.webkitTransition = 'transform .5s'
        }
        function setTranslateX(x) {
          ul.style.transform = 'translateX(' + x + 'px)'
          ul.style.webkitTransform = 'translateX(' + x + 'px)'
        }
        function turn() {
          index++
          addTransition()
          setTranslateX(-index * width)
        }

        window.addEventListener('resize', function() {
          width = banner.offsetWidth //重新获取banner宽度
          //让ul重新移动一下
          removeTransition() //去掉过渡效果
          setTranslateX(-index * width) //ul位移
        })
      }
    })()
  }
}
</script>

